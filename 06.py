#Arithmetic operator
a=45
b=34
print("Sum of number is",a+b)
print("Sub of number is",a-b)
print("mul of number is",a*b)
print("divide of number is",a/b)
print("mod of number is",a%b)

#Assignment operator
a=55
a+=2
print(a)
a-=6
print(a)
a/=3
print(a)
a%=2
print(a)

#comparison operator
b=(18<=5)
print(b)
c=(18>=5)
print(c)
d=(18==5)
print(d)
e=(18!=5)
print(e)

#Logical operator
bool1=True
bool2=False
print("value of bool1 and bool2", (bool1 and bool2))
